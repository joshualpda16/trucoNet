    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        lblNombreSala.setText(claseGeneral.getNombreSala());
        if(claseGeneral.isSoyServer()){
            lblOponente.setText("Nombre del cliente:");
            lblNombreOponente.setText(claseGeneral.lstJugadores.get(1).getNombre());
            cmdListo.setEnabled(false);
        } else{
            lblOponente.setText("Nombre del servidor:");
            lblNombreOponente.setText(claseGeneral.lstJugadores.get(0).getNombre());
            cmdListo.setEnabled(true);
        }
    }//GEN-LAST:event_formInternalFrameOpened

    private void txtEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEnviarActionPerformed
        if(claseGeneral.isSoyServer()){
            SimpleServer.enviarDatos("CHP"+"0"+txtEnviar.getText());
        } else{
            SimpleClient.enviarDatos("CHP"+"1"+txtEnviar.getText());
        }
        
        SimpleAttributeSet attrs = new SimpleAttributeSet();
        StyleConstants.setBold(attrs, true);
        try {
            //El nombre primero en negrita
            txtChatPrevio.getStyledDocument().insertString(txtChatPrevio.getStyledDocument().getLength(),claseGeneral.getMiNombre()+" > ",attrs);
            StyleConstants.setBold(attrs, false);
            txtChatPrevio.getStyledDocument().insertString(txtChatPrevio.getStyledDocument().getLength(),txtEnviar.getText()+"\n",attrs);
        } catch (BadLocationException ex) {
            Logger.getLogger(claseGeneral.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtEnviar.setText("");
    }//GEN-LAST:event_txtEnviarActionPerformed

    private void cmdListoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdListoActionPerformed
        if(claseGeneral.isSoyServer()){
            SimpleServer.enviarDatos("CHP0ready");
            
            List<Carta> lst6Cartas=new ArrayList();
            
            lst6Cartas = Juego.crearCartasRandom();
            
            claseGeneral.lstJugadores.get(0).setCartas(Juego.separarCartas(lst6Cartas,1));
            claseGeneral.lstJugadores.get(1).setCartas(Juego.separarCartas(lst6Cartas,2));
            
            Jugador j1 = (Jugador) claseGeneral.lstJugadores.get(1);
            Jugador j0 = (Jugador) claseGeneral.lstJugadores.get(0);
            
            SimpleServer.enviarDatos("JGOCCTS"+j1.cartasToString(j1.getCartas()));
            SimpleServer.enviarDatos("JGOSCTS"+j0.cartasToString(j0.getCartas()));
            
            claseGeneral.abrirJuego();
            claseGeneral.miJuego = new Juego();
        } else{
            try{
                SimpleClient.enviarDatos("CHP1ready");
                cmdListo.setEnabled(false);
                SimpleAttributeSet attrs = new SimpleAttributeSet();
                StyleConstants.setBold(attrs, true);
                txtChatPrevio.getStyledDocument().insertString(txtChatPrevio.getStyledDocument().getLength(),"Estas listo. Esper√° al servidor.\n",attrs);
            } catch (BadLocationException ex) {
                Logger.getLogger(frmChatPrevio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_cmdListoActionPerformed

